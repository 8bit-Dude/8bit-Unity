
;;; source file for CC65 "remake" compiler
	.include "soundbs.mac"
	.global musicptr
	.rodata
musicptr:
	.word CHANNEL_0, CHANNEL_1, 0, 0

PATTERN_LENGTH   .set  3840

CHANNEL_0:
  DO 0
	PATTERN ENVIROS
	PATTERN PATTERN_01
  LOOP
CHAN_STOP

CHANNEL_1:
  DO 0
	PATTERN ENVIROS
	PATTERN PATTERN_02
  LOOP
CHAN_STOP

ENVIROS:
	SETPLAYERFREQ $9D,129
	DEF_WAVEENV 1,WAVEENV_1
	DEF_VOLENV 2,TREMOLO_2
	DELAY 3
RETURN

WAVEENV_1:
.byte 1,3,8,$28,$0,$5d,8,$f0,$0,$80,8,$69,$10,$4c
TREMOLO_2:
.byte $02,$02,$05,$00,$01,$FB



PATTERN_01:
	INSTR2 $68,$00,$5f,44,127
	SET_WAVEENV 1
	PLAY2 224,$7c,255
	DELAY 593
	PLAY2 212,$7c,255
	DELAY 1017
	PLAY2 224,$7c,255
	DELAY 1017
RETURN


PATTERN_02:
	DELAY 848
	INSTR2 $60,$20,$7b,56,127
	SET_VOLENV 2
	PLAY2 2,$59,212
	PLAY2 2,$59,53
	PLAY2 5,$58,159
	PLAY2 2,$59,255
	DELAY 593
	PLAY2 2,$59,212
	PLAY2 2,$59,53
	PLAY2 5,$58,159
	PLAY2 2,$59,255
	DELAY 63
	PLAY2 2,$59,53
	PLAY2 2,$59,53
	PLAY2 2,$59,53
	PLAY2 2,$59,53
	PLAY2 2,$59,255
	DELAY 63
RETURN
